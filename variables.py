# string
name = "Anurag"
print(type(name))
# integer
roll_no = 13
print(type(roll_no))
# floating number
percentage = 99.3
print(type(percentage))
# boolean
is_student = True
print(type(is_student))
print(name, roll_no, percentage, is_student)
# concatenation
print("My name is " + name + " and my roll no is", roll_no)
print("I scored", percentage,"in my final exams. I am a student is ",is_student)
# print with separator
print(name, roll_no, percentage, is_student, sep="->")